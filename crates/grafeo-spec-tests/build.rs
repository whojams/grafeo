fn sanitize_module_name(path: &str) -> String {
    path.replace('/', "_")
        .replace(['.', '-', ' '], "_")
        .to_lowercase()
}

fn sanitize_test_name(name: &str) -> String {
    name.chars()
        .map(|c| {
            if c.is_alphanumeric() || c == '_' {
                c
            } else {
                '_'
            }
        })
        .collect::<String>()
        .to_lowercase()
}

fn escape_rust_string(s: &str) -> String {
    s.replace('\\', "\\\\")
        .replace('"', "\\\"")
        .replace('\n', "\\n")
        .replace('\r', "\\r")
        .replace('\t', "\\t")
}

fn generate_tests(
    output: &mut String,
    file: &GtestFile,
    module_name: &str,
    rel_path: &str,
) -> usize {
    let mut count = 0;

    writeln!(output, "mod {module_name} {{").unwrap();
    writeln!(output, "    #[allow(unused_imports)]").unwrap();
    writeln!(output, "    use super::*;").unwrap();
    writeln!(output).unwrap();

    for tc in &file.tests {
        // Skip tests with skip field
        if tc.skip.is_some() {
            writeln!(
                output,
                "    #[test]\n    #[ignore = \"skipped in .gtest\"]\n    fn {}() {{}}\n",
                sanitize_test_name(&tc.name)
            )
            .unwrap();
            count += 1;
            continue;
        }

        // Handle rosetta variants
        if !tc.variants.is_empty() {
            for (lang, query) in &tc.variants {
                let fn_name = format!(
                    "{}_{}",
                    sanitize_test_name(&tc.name),
                    sanitize_test_name(lang)
                );
                generate_single_test(
                    output,
                    &fn_name,
                    file,
                    tc,
                    Some(query),
                    Some(lang),
                    rel_path,
                );
                count += 1;
            }
        } else {
            generate_single_test(
                output,
                &sanitize_test_name(&tc.name),
                file,
                tc,
                None,
                None,
                rel_path,
            );
            count += 1;
        }
    }

    writeln!(output, "}}").unwrap();
    writeln!(output).unwrap();

    count
}

fn generate_single_test(
    output: &mut String,
    fn_name: &str,
    file: &GtestFile,
    tc: &TestCase,
    query_override: Option<&str>,
    lang_override: Option<&str>,
    rel_path: &str,
) {
    let language = lang_override
        .or(tc.language.as_deref())
        .unwrap_or(&file.meta.language);
    let model = &file.meta.model;

    // Feature gate: languages that require specific feature flags.
    // graphql-rdf needs both "graphql" and "rdf" features.
    let feature_gates: Vec<&str> = match language {
        "cypher" => vec!["cypher"],
        "gremlin" => vec!["gremlin"],
        "graphql" => vec!["graphql"],
        "graphql-rdf" => vec!["graphql", "rdf"],
        "sparql" => vec!["sparql"],
        "sql-pgq" | "sql_pgq" => vec!["sql-pgq"],
        _ => vec![],
    };

    // Requires from file meta + per-test requires
    let mut all_requires: Vec<&str> = file.meta.requires.iter().map(String::as_str).collect();
    for req in &tc.requires {
        if !all_requires.contains(&req.as_str()) {
            all_requires.push(req);
        }
    }
    for fg in feature_gates {
        if !all_requires.contains(&fg) {
            all_requires.push(fg);
        }
    }

    // Only emit #[cfg] for known Cargo features. Binding capabilities
    // (e.g. "int64-safe") are not Cargo features and the Rust runner
    // always has full fidelity, so they pass through without gating.
    const CARGO_FEATURES: &[&str] = &[
        "gql",
        "cypher",
        "sparql",
        "gremlin",
        "graphql",
        "sql-pgq",
        "rdf",
        "algos",
        "vector-index",
        "text-index",
        "hybrid-search",
        "cdc",
        "tiered-storage",
        "succinct-indexes",
        "ring-index",
        "block-stm",
        "wal",
        "spill",
        "mmap",
    ];
    let cargo_requires: Vec<&str> = all_requires
        .iter()
        .filter(|r| CARGO_FEATURES.contains(r))
        .copied()
        .collect();

    if !cargo_requires.is_empty() {
        let cfg_parts: Vec<String> = cargo_requires
            .iter()
            .map(|r| format!("feature = \"{r}\""))
            .collect();
        let cfg = if cfg_parts.len() == 1 {
            cfg_parts[0].clone()
        } else {
            format!("all({})", cfg_parts.join(", "))
        };
        writeln!(output, "    #[cfg({cfg})]").unwrap();
    }

    writeln!(output, "    #[test]").unwrap();
    writeln!(output, "    fn {fn_name}() {{").unwrap();

    // Create DB and load dataset
    writeln!(output, "        let db = GrafeoDB::new_in_memory();").unwrap();

    let effective_dataset = tc.dataset.as_deref().unwrap_or(&file.meta.dataset);
    if effective_dataset != "empty" {
        let dataset_path = format!("tests/spec/datasets/{}.setup", effective_dataset);
        writeln!(
            output,
            "        load_dataset(&db, \"{}\");",
            escape_rust_string(&dataset_path)
        )
        .unwrap();
    }

    // Setup queries run in the file's declared language and model. This allows
    // SPARQL tests to use INSERT DATA in setup, Cypher tests to use CREATE, etc.
    // For rosetta variant tests, setup always uses meta.language (the file default).
    let setup_language = &file.meta.language;
    for setup_q in &tc.setup {
        writeln!(
            output,
            "        execute_query_with_model(&db, \"{}\", \"{}\", \"{}\");",
            escape_rust_string(setup_language),
            escape_rust_string(model),
            escape_rust_string(setup_q)
        )
        .unwrap();
    }

    // Determine the query to execute
    let query = query_override
        .map(String::from)
        .or_else(|| tc.query.clone());
    let queries = if !tc.statements.is_empty() {
        tc.statements.clone()
    } else if let Some(q) = &query {
        vec![q.clone()]
    } else {
        writeln!(
            output,
            "        panic!(\"No query or statements in test '{}' at {}\");",
            tc.name, rel_path
        )
        .unwrap();
        writeln!(output, "    }}").unwrap();
        writeln!(output).unwrap();
        return;
    };

    // Execute queries (all but last are fire-and-forget, last captures result)
    if queries.len() == 1 {
        generate_execute_and_assert(output, &queries[0], language, model, tc, false);
    } else {
        let has_params = !tc.params.is_empty();

        // Build params once before the loop so fire-and-forget statements
        // can use them too (e.g. INSERT/SET with $param).
        if has_params {
            generate_params_hashmap(output, &tc.params);
        }

        // Execute all but last (with params so $param in INSERT/SET works)
        for q in &queries[..queries.len() - 1] {
            if has_params {
                writeln!(
                    output,
                    "        execute_query_result_with_params(&db, \"{}\", \"{}\", \"{}\", params.clone()).expect(\"setup statement failed\");",
                    escape_rust_string(language),
                    escape_rust_string(model),
                    escape_rust_string(q),
                )
                .unwrap();
            } else {
                writeln!(
                    output,
                    "        execute_query_with_model(&db, \"{}\", \"{}\", \"{}\");",
                    escape_rust_string(language),
                    escape_rust_string(model),
                    escape_rust_string(q)
                )
                .unwrap();
            }
        }
        // Last query: capture result and assert
        let last = &queries[queries.len() - 1];
        generate_execute_and_assert(output, last, language, model, tc, has_params);
    }

    writeln!(output, "    }}").unwrap();
    writeln!(output).unwrap();
}

fn generate_params_hashmap(output: &mut String, params: &HashMap<String, String>) {
    writeln!(output, "        let params = {{").unwrap();
    writeln!(
        output,
        "            let mut m = std::collections::HashMap::new();"
    )
    .unwrap();
    for (key, value) in params {
        if let Ok(n) = value.parse::<i64>() {
            writeln!(
                output,
                "            m.insert(\"{}\".to_string(), grafeo_common::types::Value::Int64({n}));",
                escape_rust_string(key)
            )
            .unwrap();
        } else if let Ok(f) = value.parse::<f64>() {
            writeln!(
                output,
                "            m.insert(\"{}\".to_string(), grafeo_common::types::Value::Float64({f}_f64));",
                escape_rust_string(key)
            )
            .unwrap();
        } else if value == "true" || value == "false" {
            writeln!(
                output,
                "            m.insert(\"{}\".to_string(), grafeo_common::types::Value::Bool({value}));",
                escape_rust_string(key)
            )
            .unwrap();
        } else {
            writeln!(
                output,
                "            m.insert(\"{}\".to_string(), grafeo_common::types::Value::String(\"{}\".into()));",
                escape_rust_string(key),
                escape_rust_string(value)
            )
            .unwrap();
        }
    }
    writeln!(output, "            m").unwrap();
    writeln!(output, "        }};").unwrap();
}

fn generate_execute_and_assert(
    output: &mut String,
    query: &str,
    language: &str,
    model: &str,
    tc: &TestCase,
    params_already_emitted: bool,
) {
    let expect = &tc.expect;
    let has_params = !tc.params.is_empty();

    // Build params HashMap if test has parameters and not already emitted
    // (multi-statement tests emit params before the fire-and-forget loop).
    if has_params && !params_already_emitted {
        generate_params_hashmap(output, &tc.params);
    }

    // Error case
    if let Some(err_substr) = &expect.error {
        if has_params {
            writeln!(
                output,
                "        let result = execute_query_result_with_params(&db, \"{}\", \"{}\", \"{}\", params);",
                escape_rust_string(language),
                escape_rust_string(model),
                escape_rust_string(query)
            )
            .unwrap();
        } else {
            writeln!(
                output,
                "        let result = execute_query_result(&db, \"{}\", \"{}\", \"{}\");",
                escape_rust_string(language),
                escape_rust_string(model),
                escape_rust_string(query)
            )
            .unwrap();
        }
        writeln!(
            output,
            "        assert!(result.is_err(), \"Expected error containing '{}' but query succeeded\");",
            escape_rust_string(err_substr)
        )
        .unwrap();
        writeln!(
            output,
            "        let err_msg = format!(\"{{}}\", result.unwrap_err());",
        )
        .unwrap();
        writeln!(
            output,
            "        assert!(err_msg.contains(\"{}\"), \"Error '{{}}' does not contain '{}'\", err_msg);",
            escape_rust_string(err_substr),
            escape_rust_string(err_substr)
        )
        .unwrap();
        return;
    }

    // Normal execution
    if has_params {
        writeln!(
            output,
            "        let result = execute_query_result_with_params(&db, \"{}\", \"{}\", \"{}\", params).expect(\"query failed\");",
            escape_rust_string(language),
            escape_rust_string(model),
            escape_rust_string(query)
        )
        .unwrap();
    } else {
        writeln!(
            output,
            "        let result = execute_query_result(&db, \"{}\", \"{}\", \"{}\").expect(\"query failed\");",
            escape_rust_string(language),
            escape_rust_string(model),
            escape_rust_string(query)
        )
        .unwrap();
    }

    // Column assertion (checked before value assertions)
    if !expect.columns.is_empty() {
        let cols: Vec<String> = expect
            .columns
            .iter()
            .map(|c| format!("\"{}\"", escape_rust_string(c)))
            .collect();
        writeln!(
            output,
            "        assert_columns(&result, &[{}]);",
            cols.join(", ")
        )
        .unwrap();
    }

    // Empty check
    if expect.empty {
        writeln!(
            output,
            "        assert_eq!(result.row_count(), 0, \"Expected empty result\");"
        )
        .unwrap();
        return;
    }

    // Count check
    if let Some(count) = expect.count {
        writeln!(
            output,
            "        assert_eq!(result.row_count(), {count}, \"Expected {count} row(s)\");"
        )
        .unwrap();
        return;
    }

    // Hash check
    if let Some(hash) = &expect.hash {
        writeln!(
            output,
            "        assert_hash(&result, \"{}\");",
            escape_rust_string(hash)
        )
        .unwrap();
        return;
    }

    // Rows check
    if !expect.rows.is_empty() {
        // Build expected rows literal
        writeln!(output, "        let expected: Vec<Vec<String>> = vec![").unwrap();
        for row in &expect.rows {
            let cols: Vec<String> = row
                .iter()
                .map(|c| format!("\"{}\".to_string()", escape_rust_string(c)))
                .collect();
            writeln!(output, "            vec![{}],", cols.join(", ")).unwrap();
        }
        writeln!(output, "        ];").unwrap();

        if let Some(precision) = expect.precision {
            writeln!(
                output,
                "        assert_rows_with_precision(&result, &expected, {precision});"
            )
            .unwrap();
        } else if expect.ordered {
            writeln!(output, "        assert_rows_ordered(&result, &expected);").unwrap();
        } else {
            writeln!(output, "        assert_rows_sorted(&result, &expected);").unwrap();
        }
    }
}
